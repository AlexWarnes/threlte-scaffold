export class ColorGUIHelper {
  object: any;
  prop: string;
  constructor(object: any, prop: string) {
    this.object = object;
    this.prop = prop;
  }
  get value() {
    return `#${this.object[this.prop].getHexString()}`;
  }
  set value(hexString) {
    this.object[this.prop].set(hexString);
  }
}